package com.simple.bsp.help.po;

/**
 * @author simple
 *
 */
public class HelpInfo{
	
	private String id;
	private String helpQuery;
	private String helpShow;
	private String helpSql;
	private String helpColum;
	
	private String queryKey;
	private String queryValue;
	
	//动态条件
	private String param1;
	private String param2;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getHelpQuery() {
		return helpQuery;
	}
	public void setHelpQuery(String helpQuery) {
		this.helpQuery = helpQuery;
	}
	public String getHelpShow() {
		return helpShow;
	}
	public void setHelpShow(String helpShow) {
		this.helpShow = helpShow;
	}
	public String getHelpSql() {
		return helpSql;
	}
	public void setHelpSql(String helpSql) {
		this.helpSql = helpSql;
	}
	public String getHelpColum() {
		return helpColum;
	}
	public void setHelpColum(String helpColum) {
		this.helpColum = helpColum;
	}
	public String getQueryKey() {
		return queryKey;
	}
	public void setQueryKey(String queryKey) {
		this.queryKey = queryKey;
	}
	public String getQueryValue() {
		return queryValue;
	}
	public void setQueryValue(String queryValue) {
		this.queryValue = queryValue;
	}
	public String getParam1() {
		return param1;
	}
	public void setParam1(String param1) {
		this.param1 = param1;
	}
	public String getParam2() {
		return param2;
	}
	public void setParam2(String param2) {
		this.param2 = param2;
	}
	
	

}
